public class switchCase  {

	public void switch1(int errorCode) {
		switch (errorCode) {
		case 1:
			// do logic of status 1
			break;

		case 2:
			// do logic of status 2
			break;
		case 3:
			// do logic of status 3
			break;
		case 4:
			// do logic of status 4
			break;
		default:
			break;
		}
	}

}
